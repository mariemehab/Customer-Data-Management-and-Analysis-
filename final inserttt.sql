INSERT INTO payment (payment_id, order_id, payment_date, amount, payment_method)
SELECT
    ROW_NUMBER() OVER (ORDER BY o.order_id) AS payment_id,
    o.order_id,
    o.order_date AS payment_date, -- Use order_date as payment_date
    o.total_amount, -- Amount from orders
    CASE 
        WHEN (ROW_NUMBER() OVER (ORDER BY o.order_id) % 3) = 0 THEN 'credit_card'
        WHEN (ROW_NUMBER() OVER (ORDER BY o.order_id) % 3) = 1 THEN 'paypal'
        ELSE 'bank_transfer'
    END AS payment_method -- Alternate payment methods
FROM
    orders o
WHERE
    o.order_id BETWEEN 1 AND 100; -- Ensure to select only orders from 1 to 100

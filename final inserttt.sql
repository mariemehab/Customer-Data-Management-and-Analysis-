INSERT INTO transactions (transaction_id, customer_id, seller_id, transaction_date, amount)
SELECT
    ROW_NUMBER() OVER (ORDER BY (SELECT NULL)) AS transaction_id, -- Generate transaction_id from 1 to 100
    (ROW_NUMBER() OVER (ORDER BY (SELECT NULL)) - 1) % 100 + 1 AS customer_id, -- Customer ID from 1 to 100
    (ROW_NUMBER() OVER (ORDER BY (SELECT NULL)) - 1) % 20 + 1 AS seller_id, -- Seller ID from 1 to 20
    o.order_date AS transaction_date, -- Use order_date as transaction_date
    o.total_amount -- Amount from orders
FROM
    orders o
WHERE
    o.order_id BETWEEN 1 AND 100 -- Ensure to select only orders from 1 to 100
ORDER BY
    transaction_id
